import math


def main(x, y, z):
    return math.sqrt(((math.asin(y) ** 3) + (86 * ((z / 56)
                                                   + (52 * x ** 3)) ** 5)) /
                     (46 * (1 - (89 * y ** 3) - 51 * z) - (math.exp(34 * z ** 3 - x ** 2) ** 4))) - \
        ((1 + z ** 2) ** 4 - (26 * x + 46 * y ** 3 + 1) ** 3)

print(main(-0.78, -0.18, 0.94)) # -7.48e+03
print(main(0.38, -0.24, -0.28)) # 1.08e+03
print(main(-0.2, -0.53, 0.64)) # -1.35e+03
print(main(0.89, 0.07, -0.13)) # 1.81e+04
print(main(0.36, -0.74, -0.91)) # -5.78e+02
